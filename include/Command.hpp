#ifndef COMMAND_H
#define COMMAND_H

enum Device {  //
  TUNER = 0b010110,
  TV = 0,  // No signal on bus
  AMP = 0b011001,
  TAPE = 0b100101,
  VCR = 0b100110,
  PHONO = 0b010110,
  CD = 0b010101,
  SYSTEM = 0b101001,
};

enum Tuner {
  AM = 0b101001100,
  FM = 0b101001101,
  TUNE_DOWN = 0b101010101,
  TUNE_UP = 0b101010100,
  MEMORY_1 = 0b100101011,
  MEMORY_2 = 0b100101100,
  MEMORY_3 = 0b100101101,
  MEMORY_4 = 0b100110010,
  MEMORY_5 = 0b100110011,
  MEMORY_6 = 0b100110100,
  MEMORY_7 = 0b100110101,
  MEMORY_8 = 0b101001010,
};

enum Amp {
  VOLUME_DOWN_FRONT = 0b010101100,
  VOLUME_UP_FRONT = 0b010101011,
  VOLUME_DOWN_REAR = 0b101010011,
  VOLUME_UP_REAR = 0b011010011,
  MUTE = 0b011010010,
  INPUT_TUNER = 0b100110011,
  INPUT_PHONO = 0b100110101,
  INPUT_CD = 0b011010100,
  INPUT_TAPE_MON = 0b010110101,
  INPUT_TV = 0b100110100,
  INPUT_VIDEO = 0b101001010,
  INPUT_VCR = 0b101001011,
};

enum Tv {
  // No signal on bus
};

enum Tape {
  PLAY_RPT = 0b010101011,
  STOP_C = 0b010110010,
  REW = 0b010101100,
  FF = 0b010101101,
  PAUSE = 0b010110011,
  DIRECTION = 0b011001100,
  INDEX_SCAN = 0b010110100,
  QMS = 0b011001101,
  MEMO = 0b011001010,
  REC = 0b011010011,
  REC_MUTE = 0b010110101,
};

enum Vcr {
  VCR_DIRECTION = Tape::DIRECTION,
  VCR_MEMO = Tape::MEMO,
  VCR_REC_MUTE = Tape::REC_MUTE,
};

enum Phono {
  PLAY = 0b010101011,
  CUT = 0b010110010,
  CUE = Tape::PAUSE,
  REPEAT = 0b011001011,
};

enum Cd {  //
  PLAY_NEXT = Phono::PLAY,
  STOP_C = Phono::CUT,
  CD_PAUSE = Tape::PAUSE,
  CD_REPEAT = Phono::REPEAT
};

class Command {
 public:
  Command();
  ~Command();

 private:
};

#endif  // COMMAND_H